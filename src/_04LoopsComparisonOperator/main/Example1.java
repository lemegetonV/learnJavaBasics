package _04LoopsComparisonOperator.main;

public class Example1 {

    public static void main(String[] args) {
        // < > <= >= == !=

        boolean b1 = 10 < 20; // true
        boolean b2 = 10 > 20; // false
        boolean b3 = 10 == 20; // false
        boolean b4 = 10 != 20; // true
        boolean b5 = 10 <= 10; // true
        boolean b6 = 10 >= 10; // true

        boolean b7 = 'a' < 'A'; // false
        boolean b8 = 'a' >= 'b'; // false



    }
}
